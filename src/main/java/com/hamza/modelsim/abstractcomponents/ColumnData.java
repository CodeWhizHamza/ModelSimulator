package com.hamza.modelsim.abstractcomponents;

public class ColumnData {
    public String key;
    public int value;

    public ColumnData(String key, int value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
